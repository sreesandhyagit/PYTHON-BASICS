# list
print("----------------list----------------")
myList=["hai",3,True,4.9]
for x in myList:
    print(x)

# tuple
print("----------------tuple---------------")
myTuple=("hello",2,3.1,False)
for x in myTuple:
    print(x)

# set
print("----------------set-----------------")
mySet={2,"sree",6.7}
for x in mySet:
    print(x,)

# dictionary
print("----------------dictionary--------------")
for d in {"name":"sree","age":30}:
    print(d)

print("----------------dictionary--------------")
stud={"name":"sree","age":30}
for d in stud:
    print(d,":",stud[d])

